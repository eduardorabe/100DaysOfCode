print("Welcome to Treasure Island.\nYour mission is to find the treasure.")

leftOrRight = input("Left or right? ")
if leftOrRight.lower() == "right":
    print("You fell into a hole.\nGame Over.")
else:
    swimOrWait = input("Swim or wait? ")
    if swimOrWait.lower() == "swim":
        print("You were attacked by a trout. \nGame Over.")
    else:
        redOrBlue = input("Red, blue or yellow door? ")
        if redOrBlue.lower() == "red":
            print("You were burned by fire.\nGame Over.")
        elif redOrBlue.lower() == "blue":
            print("You were eaten by beasts.\nGame Over.")
        elif redOrBlue.lower() == "yellow":
            print("You Win!")
        else:
            print("Game Over.")